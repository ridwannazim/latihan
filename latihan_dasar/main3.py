# DICTIONARY

# list -> array, mengakses dengan menggubakab indeks
list_ = ['ajib','ijab','ijub']
print(list_[1])

# dictionary (dict) -> associative array
# identifier -> key

data_dict ={
    'key':'value',
    'ai':'ajib',
    'ia':'ijab',
    'iu':'ijub',
    'ank':20,
    'list':list_,
}

print(data_dict['ai'])
print(data_dict['ank'])
print(data_dict['list'])